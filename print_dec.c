#include "ft_printf.h"

void	print_dec(int n)
{
	char c;

	if (n < 0)
	{
		n *= (-1);
		write(1, "-",1);
		print_dec(n);
	}
	else if (n <= 9)
	{
		c = n + '0';
		write(1, &c, 1);
	}
	else
	{
		print_dec(n / 10);
		print_dec(n % 10);
	}
}
